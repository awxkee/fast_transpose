#[cfg(target_arch = "x86")]
use std::arch::x86::*;
#[cfg(target_arch = "x86_64")]
use std::arch::x86_64::*;

#[inline(always)]
pub(crate) unsafe fn _mm_storeu_si64(mem_addr: *mut u8, v: __m128i) {
    std::ptr::copy_nonoverlapping(&v as *const _ as *const u8, mem_addr, 8);
}

#[inline(always)]
pub(crate) unsafe fn _mm_loadu_si128_u16(mem: &[u16]) -> __m128i {
    _mm_loadu_si128(mem.as_ptr() as *const __m128i)
}

#[inline(always)]
pub(crate) unsafe fn _mm_storeu_si128_u16(mem_addr: &mut [u16], v: __m128i) {
    _mm_storeu_si128(mem_addr.as_ptr() as *mut __m128i, v);
}
